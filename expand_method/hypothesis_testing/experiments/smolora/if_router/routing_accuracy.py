#!/usr/bin/env python3
"""
Option A: SMoLoRA IF Router — Routing Accuracy Evaluation.

Tests whether SRT Mahalanobis routing outperforms cosine similarity
on the SAME instruction embeddings used by SMoLoRA's IF router.

Data source: SMoLoRA ins_emb_single.pkl
  (generated by sentence-transformers/all-MiniLM-L6-v2, 7×384 embeddings)
  Run: python scripts/generate_ins_emb.py --output ins_emb_single.pkl

Metrics (routing accuracy, NOT VQA accuracy):
  - Macro routing accuracy on synthetic test samples around centroids
  - Baseline: Cosine similarity on same embeddings (proxy for original IF gate)

Usage:
    # Generate embeddings (first time only)
    python scripts/generate_ins_emb.py --output ins_emb_single.pkl

    # Run routing accuracy test
    python experiments/smolora/if_router/routing_accuracy.py \
        --ins_emb ins_emb_single.pkl \
        --task_order ScienceQA TextVQA GQA VQAv2 Flickr30k ImageNet Place365 \
        --shrinkage ridge \
        --device cuda
"""
from __future__ import annotations
import argparse
import json
import pickle
import sys
from pathlib import Path

import numpy as np

sys.path.insert(0, str(Path(__file__).parent.parent.parent.parent))
from srt_router.pooled_mahalanobis import PooledMahalanobisRouter
from srt_router.metrics import compute_routing_accuracy, compute_per_task_accuracy


# ── Default instruction strings (from SMoLoRA scripts/SMoLoRA/ins_gen.py) ──────
# ins_type 0–6 match SMoLoRA's ins_emb_single.pkl ordering exactly.
DEFAULT_INSTRUCTIONS = [
    "Answer with the option's letter from the given choices directly.",   # 0: ScienceQA
    "Answer the question using a single word or phrase.",                # 1: TextVQA
    "What is happening in the presented picture?\nPlease describe it in one complete sentence.",  # 2: Flickr30k
    "What is the object in the image?\nAnswer the question using a single word or phrase.",       # 3: ImageNet
    "Answer the question using a single word or phrase.",                # 4: GQA
    "Answer the question using a single word or phrase.",                # 5: VQAv2
    "What is the background of the image?\nAnswer the question using a single word or phrase.",  # 6: Place365
]

DEFAULT_TASK_ORDER = [
    "ScienceQA", "TextVQA", "GQA", "VQAv2", "Flickr30k", "ImageNet", "Place365"
]


def mean_pooling(model_output, attention_mask):
    token_embeddings = model_output[0]
    input_mask_expanded = attention_mask.unsqueeze(-1).expand(token_embeddings.size()).float()
    return torch.sum(token_embeddings * input_mask_expanded, 1) / torch.clamp(input_mask_expanded.sum(1), min=1e-9)


def _generate_ins_emb_from_strings(instructions: list, model_name: str = "sentence-transformers/all-MiniLM-L6-v2"):
    """Generate ins_emb embeddings from instruction strings (same as SMoLoRA ins_gen.py)."""
    try:
        import torch
        from transformers import AutoTokenizer, AutoModel
    except ImportError:
        return None

    tokenizer = AutoTokenizer.from_pretrained(model_name)
    model = AutoModel.from_pretrained(model_name)
    model.eval()

    encoded = tokenizer(instructions, padding=True, truncation=True, return_tensors="pt")
    with torch.no_grad():
        output = model(**encoded)
    embeddings = mean_pooling(output, encoded["attention_mask"])
    return embeddings.cpu().numpy().astype(np.float32)


def _load_ins_emb(path: str | None, task_order: list[str]) -> np.ndarray:
    """
    Load or generate ins_emb.

    1. If path provided and file exists → load pickle
    2. Else try to generate from instruction strings using Sentence-BERT
    3. Else fall back to synthetic Gaussian centroids
    """
    if path is not None and Path(path).exists():
        print(f"  Loading ins_emb from: {path}")
        with open(path, "rb") as f:
            raw = pickle.load(f)
        ins_emb = np.array(raw)
        if ins_emb.ndim == 1:
            ins_emb = ins_emb.reshape(1, -1)
        return ins_emb

    # Try to generate from instruction strings
    generated = _generate_ins_emb_from_strings(DEFAULT_INSTRUCTIONS)
    if generated is not None:
        print(f"  [Generated] ins_emb from {len(DEFAULT_INSTRUCTIONS)} instruction strings")
        print(f"    Model: sentence-transformers/all-MiniLM-L6-v2 (384-dim)")
        return generated

    # Fallback: synthetic
    print(f"  [Synthetic] Generating {len(task_order)} Gaussian centroids (dim=384, sep=2.0)")
    rng = np.random.RandomState(42)
    d = 384
    n = len(task_order)
    embs = []
    for i in range(n):
        direction = rng.randn(d)
        direction /= (np.linalg.norm(direction) + 1e-12)
        centroid = direction * 2.0 * i + rng.randn(d) * 0.1
        embs.append(centroid.astype(np.float32))
    return np.array(embs)


def parse_args():
    parser = argparse.ArgumentParser(description="SMoLoRA IF Router — Routing Accuracy")
    parser.add_argument("--ins_emb", type=str, default="ins_emb_single.pkl",
                       help="Path to ins_emb_single.pkl. "
                            "Run: python scripts/generate_ins_emb.py --output ins_emb_single.pkl")
    parser.add_argument("--task_order", type=str, nargs="+",
                       default=DEFAULT_TASK_ORDER,
                       help="Task names in CL order (must match ins_emb rows 0–N)")
    parser.add_argument("--output_dir", type=str, default="results_smolora_if")
    parser.add_argument("--shrinkage", type=str, default="ridge",
                       choices=["ridge", "lw"])
    parser.add_argument("--n_samples", type=int, default=200,
                       help="Test samples per task")
    parser.add_argument("--noise_std", type=float, default=0.1,
                       help="Gaussian noise std around centroids")
    parser.add_argument("--device", type=str, default="cuda",
                       choices=["cuda", "cpu"])
    return parser.parse_args()


def main():
    args = parse_args()
    task_order = args.task_order
    n_tasks = len(task_order)

    # GPU header
    try:
        import torch
        if torch.cuda.is_available():
            gpu_name = torch.cuda.get_device_name(0)
            gpu_mem = torch.cuda.get_device_properties(0).total_mem / 1e9
            gpu_tag = f"[GPU] {gpu_name}  {gpu_mem:.1f}GB"
        else:
            gpu_tag = "[CPU]"
    except Exception:
        gpu_tag = "[CPU]"

    # Load or generate ins_emb
    ins_emb = _load_ins_emb(args.ins_emb, task_order)
    d = ins_emb.shape[-1]

    print(f"\n{gpu_tag}  === CL Routing [SMoLoRA IF Router] ===")
    print(f"    Tasks: {n_tasks}  ({', '.join(task_order)})")
    print(f"    ins_emb: {ins_emb.shape}  ({'real' if args.ins_emb and Path(args.ins_emb).exists() else 'generated' if args.ins_emb else 'synthetic'})")
    print(f"    Shrinkage: {args.shrinkage}  Device: {args.device}")
    print(f"    Noise std: {args.noise_std}  N samples/task: {args.n_samples}")
    print(f"    ins_emb dim: {d}")

    # Router
    srt_router = PooledMahalanobisRouter(shrinkage=args.shrinkage, device=args.device)
    print(f"  Router: {srt_router.device}  {'[GPU]' if srt_router.device == 'cuda' else '[CPU]'}")

    print(f"\n{'='*75}")
    print(f"  SMoLoRA IF Router — Routing Accuracy")
    print(f"  Tasks: {n_tasks}  Shrinkage: {args.shrinkage}  Device: {args.device}")
    print(f"  Baseline: Cosine similarity (proxy for original IF gate)")
    print(f"  Test:    SRT Mahalanobis (SRT Theorem 4)")
    print(f"{'='*75}\n")

    all_results = []

    for step_idx, task_name in enumerate(task_order):
        srt_router.add_task(ins_emb[step_idx].reshape(1, -1), task_name=task_name)

        all_preds_srt = []
        all_preds_cos = []
        all_gt = []

        for t_idx in range(step_idx + 1):
            # Synthetic test: Gaussian noise around centroid
            rng = np.random.RandomState(42 + t_idx)
            test_embs = ins_emb[t_idx].reshape(1, -1) + rng.randn(args.n_samples, d) * args.noise_std
            gts = np.full(args.n_samples, t_idx, dtype=np.int64)
            all_gt.append(gts)

            preds_srt = srt_router.route(test_embs)
            all_preds_srt.append(preds_srt)

            # Cosine baseline on same centroids
            centroids = np.stack([ins_emb[i] for i in range(step_idx + 1)])
            c_n = centroids / (np.linalg.norm(centroids, axis=1, keepdims=True) + 1e-12)
            t_n = test_embs / (np.linalg.norm(test_embs, axis=1, keepdims=True) + 1e-12)
            preds_cos = (t_n @ c_n.T).argmax(axis=1).astype(np.int64)
            all_preds_cos.append(preds_cos)

        all_preds_srt = np.concatenate(all_preds_srt)
        all_preds_cos = np.concatenate(all_preds_cos)
        all_gt = np.concatenate(all_gt)

        acc_srt = compute_routing_accuracy(all_preds_srt, all_gt)
        acc_cos = compute_routing_accuracy(all_preds_cos, all_gt)

        seen_names = task_order[:step_idx + 1]
        pt_srt = compute_per_task_accuracy(all_preds_srt, all_gt, seen_names)
        pt_cos = compute_per_task_accuracy(all_preds_cos, all_gt, seen_names)

        diag = srt_router.get_diagnostics()
        pool_n = diag["n_pool"]

        all_results.append({
            "task": task_name,
            "step": step_idx + 1,
            "n_pool": pool_n,
            "n_d": pool_n / d if d > 0 else 0,
            "acc_srt": acc_srt,
            "acc_cos": acc_cos,
            "delta": acc_srt - acc_cos,
            "pt_srt": pt_srt,
            "pt_cos": pt_cos,
        })

        def fmt(pt):
            return "  ".join(f"{v*100:5.1f}%" for v in pt.values())

        print(f"  [{step_idx+1}/{n_tasks}] {task_name}  (pool={pool_n}, n/d={pool_n/d:.4f})")
        print(f"    RESULT Cosine (ORIGINAL)              macro={acc_cos*100:6.2f}%  [{fmt(pt_cos)}]")
        print(f"    RESULT SRT-Mahal_{args.shrinkage.upper()} (NEW)        macro={acc_srt*100:6.2f}%  [{fmt(pt_srt)}]")
        delta = (acc_srt - acc_cos) * 100
        print(f"    Delta                              {delta:+6.2f}%")

    # Final summary
    n = len(all_results)
    print(f"\n{'='*75}")
    print(f"  SMoLoRA IF — Final Routing Accuracy ({n} tasks, {args.device})")
    print(f"{'='*75}")
    hdr = "  " + "  ".join(f"{'T'+str(i+1):>7}" for i in range(n))
    print(f"  {'Method':30}  {'Final':>7}  {'Avg':>7}{hdr}")
    print(f"  {'-'*75}")

    def ts(key):
        return "  ".join(f"{r[key]*100:6.1f}%" for r in all_results)
    def av(key):
        return f"{np.mean([r[key] for r in all_results])*100:6.1f}%"
    def fn(key):
        return f"{all_results[-1][key]*100:6.1f}%"

    print(f"  {'Cosine (ORIGINAL)':30}  {fn('acc_cos')}  {av('acc_cos')}  {ts('acc_cos')}")
    print(f"  {'SRT-Mahal_{args.shrinkage.upper()} (NEW)':30}  {fn('acc_srt')}  {av('acc_srt')}  {ts('acc_srt')}")

    deltas = [all_results[i]["delta"] * 100 for i in range(n)]
    ds = "  ".join(f"{d:+5.1f}%" for d in deltas)
    print(f"  {'Delta (SRT - Original)':30}  {deltas[-1]:+6.1f}%  {np.mean(deltas):+6.1f}%  {ds}")
    print(f"{'='*75}")

    # Save
    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / f"smolora_if_{args.shrinkage}_{args.device}.json"

    has_real = args.ins_emb and Path(args.ins_emb).exists()
    report = {
        "method": "SMoLoRA IF Router (SRT Hypothesis Testing)",
        "ins_emb_mode": "real" if has_real else "generated",
        "n_tasks": n_tasks,
        "task_order": task_order,
        "n_samples_per_task": args.n_samples,
        "noise_std": args.noise_std,
        "shrinkage": args.shrinkage,
        "device": args.device,
        "results": {
            f"step_{i+1}": {
                "task": r["task"],
                "pool_size": r["n_pool"],
                "accuracy_srt": r["acc_srt"],
                "accuracy_cosine": r["acc_cos"],
                "delta": r["delta"],
                "per_task_srt": r["pt_srt"],
                "per_task_cosine": r["pt_cos"],
            }
            for i, r in enumerate(all_results)
        },
    }
    with open(out_path, "w") as f:
        json.dump(report, f, indent=2, default=str)
    print(f"\n✓ Results saved to {out_path}")


if __name__ == "__main__":
    main()
